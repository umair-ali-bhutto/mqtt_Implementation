package com.ag.fuel.model;

public class ProductPriceViewModel {

	private String dealername;
	private String tid;
	private String productname;
	private String currenteffdate;
	private String crprice;
	private String futureeffdate;
	private String cftprice;
	private String city;

	public String getDealername() {
		return dealername;
	}

	public void setDealername(String dealername) {
		this.dealername = dealername;
	}

	public String getTid() {
		return tid;
	}

	public void setTid(String tid) {
		this.tid = tid;
	}

	public String getProductname() {
		return productname;
	}

	public void setProductname(String productname) {
		this.productname = productname;
	}

	public String getCurrenteffdate() {
		return currenteffdate;
	}

	public void setCurrenteffdate(String currenteffdate) {
		this.currenteffdate = currenteffdate;
	}

	public String getCrprice() {
		return crprice;
	}

	public void setCrprice(String crprice) {
		this.crprice = crprice;
	}

	public String getFutureeffdate() {
		return futureeffdate;
	}

	public void setFutureeffdate(String futureeffdate) {
		this.futureeffdate = futureeffdate;
	}

	public String getCftprice() {
		return cftprice;
	}

	public void setCftprice(String cftprice) {
		this.cftprice = cftprice;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

}
