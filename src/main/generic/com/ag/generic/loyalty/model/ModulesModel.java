package com.ag.generic.loyalty.model;

public class ModulesModel {

	private String moduleId;
	private String moduleName;
	private String moduleRocDisplay;
	private String moduleScreenDisplay;
	private String moduleKeyWord;
	public String getModuleId() {
		return moduleId;
	}
	public void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public String getModuleRocDisplay() {
		return moduleRocDisplay;
	}
	public void setModuleRocDisplay(String moduleRocDisplay) {
		this.moduleRocDisplay = moduleRocDisplay;
	}
	public String getModuleScreenDisplay() {
		return moduleScreenDisplay;
	}
	public void setModuleScreenDisplay(String moduleScreenDisplay) {
		this.moduleScreenDisplay = moduleScreenDisplay;
	}
	public String getModuleKeyWord() {
		return moduleKeyWord;
	}
	public void setModuleKeyWord(String moduleKeyWord) {
		this.moduleKeyWord = moduleKeyWord;
	}
}
