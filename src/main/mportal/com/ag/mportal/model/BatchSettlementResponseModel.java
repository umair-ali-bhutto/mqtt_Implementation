package com.ag.mportal.model;

public class BatchSettlementResponseModel {
	
	private String settledDate;
	private String settledTime;
	private String settledCount;
	private String settledAmount;
	
	
	public String getSettledDate() {
		return settledDate;
	}
	public void setSettledDate(String settledDate) {
		this.settledDate = settledDate;
	}
	public String getSettledTime() {
		return settledTime;
	}
	public void setSettledTime(String settledTime) {
		this.settledTime = settledTime;
	}
	public String getSettledCount() {
		return settledCount;
	}
	public void setSettledCount(String settledCount) {
		this.settledCount = settledCount;
	}
	public String getSettledAmount() {
		return settledAmount;
	}
	public void setSettledAmount(String settledAmount) {
		this.settledAmount = settledAmount;
	}

}
